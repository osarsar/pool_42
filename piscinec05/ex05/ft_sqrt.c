#include <stdio.h>

int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	n;

	n = nb;
	i = 1;
	if (n > 0)
	{
		while (i * i <= n)
		{
			if (i * i == n)
				return (i);
			i++;
		}
	}
	return (0);
}
